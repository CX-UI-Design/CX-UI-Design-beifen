      //编码
      html_encode(str) {
        var s = "";
        if (str.length == 0) return "";
        s = str.replace(/&/g, ">");
        s = s.replace(/</g, "<");
        s = s.replace(/>/g, ">");
        s = s.replace(/ /g, " ");
        s = s.replace(/\'/g, "'");
        s = s.replace(/\"/g, '"');
        s = s.replace(/\n/g, "<br>");
        return s;
      },
    }
  }
</script>

